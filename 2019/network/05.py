# 平均到着パケット数　1000pps
# 平均パケット長　500bytes
# リンク容量 5Mbps

# λ(平均到着パケット数) = 平均トラヒック量[bps]/平均パケット長
λ = 1000
# 1/μ(平均サービス時間)=平均パケット長/リンク容量
μ = 1 / (8*500 / 5000000)
# 利用率
#  ρ = λ/μ
p = λ / μ
# 平均遅延時間
#  T = (1/μ) / (1-ρ)
T = (1/μ) / (1-p)

print('平均遅延時間:',T)

