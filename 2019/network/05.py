import math
# 平均呼数 0.02[calls/s]
# 平均回線保留時間 100[s]
# 回線数 5
# 呼量 a = 0.02[calls/s] * 100[s] = 2
a = 0.02 * 100
n = 5
sum=0
for i in range(1,n+1):
    sum+=(a**i)/math.factorial(i)
B = (a**n /math.factorial(n))/ (1+sum)
# 呼損率　B  =  (2^5/5!) / (1 + 2/1! + 2^2/2! + 2^3/3! + 2^4/4! + 2^5/5!)
print('呼損率:',B)
